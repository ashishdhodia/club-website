function handleSubmitISO () {
    const name = document.getElementById('resultNameISO').textContent;
    const desc = document.getElementById('resultDescISO').textContent;

    sessionStorage.setItem("NAME", name);
    sessionStorage.setItem("DESC", desc);

    return;
}
